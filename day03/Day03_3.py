# > 大于
print("3 > 1 :", 3 > 1)
print("-" * 30)

# < 小于
print("1 < 11 :", 1 < 11)
print("-" * 30)

# == 等于 注意是两个等号
print("1 == 11 :", 1 == 11)
print("-" * 30)

# ！= 不等于
print("1 != 11 :", 1 != 11)
print("-" * 30)

# >= 大于等于
print("11 >= 11 :", 11 >= 11)
print("-" * 30)

# <= 小于等于
print("11 <= 11 :", 11 <= 11)
print("-" * 30)